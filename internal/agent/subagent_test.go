func TestSpawnSubAgent_IsolatedHistory(t *testing.T) {
	// The parent agent's history should not be polluted by the sub-agent.
	parentProvider := &mockProvider{
		name:  "parent",
		model: "parent-model",
		responses: []*llm.Response{
			{Content: "parent response", StopReason: "end"},
		},
	}
	rt := newMockRuntime("/project")
	parentAgent := New(parentProvider, rt, testLogger)

	// Run parent once.
	_, err := parentAgent.Run(context.Background(), "parent message", nil)
	require.NoError(t, err)
	parentHistoryLen := len(parentAgent.History())

	// Spawn sub-agent.
	subProvider := &mockProvider{
		name:  "sub",
		model: "sub-model",
		responses: []*llm.Response{
			{Content: "sub response", StopReason: "end"},
		},
	}

	sa, err := SpawnSubAgent(context.Background(), SubAgentOpts{
		Task:     "sub task",
		Provider: subProvider,
		Runtime:  rt,
		Logger:   testLogger,
	})
	require.NoError(t, err)
	assert.Equal(t, "sub response", sa.Result)

	// Parent history should be unchanged.
	assert.Len(t, parentAgent.History(), parentHistoryLen)
}

func TestSpawnSubAgent_WithParentContext(t *testing.T) {
	subProvider := &mockProvider{
		name:  "sub",
		model: "sub-model",
		responses: []*llm.Response{
			{Content: "I used the context to complete the task.", StopReason: "end"},
		},
	}
	rt := newMockRuntime("/project")

	parentCtx := []llm.Message{
		{Role: llm.RoleUser, Content: "I'm working on the auth module"},
		{Role: llm.RoleAssistant, Content: "I see the auth module uses JWT."},
	}

	sa, err := SpawnSubAgent(context.Background(), SubAgentOpts{
		Task:      "Check JWT expiry handling",
		Provider:  subProvider,
		Runtime:   rt,
		Logger:    testLogger,
		ParentCtx: parentCtx,
	})

	require.NoError(t, err)
	assert.Equal(t, "completed", sa.Status)

	// The sub-agent's request should include the parent context.
	require.Len(t, subProvider.requests, 1)
	msgs := subProvider.requests[0].Messages
	// Should have: system, parent-context(user), ack(assistant), task(user)
	assert.GreaterOrEqual(t, len(msgs), 4)
	assert.Contains(t, msgs[1].Content, "auth module")
	assert.Contains(t, msgs[2].Content, "context from the parent")
}

