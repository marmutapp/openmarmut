func TestSubAgentManager_Kill(t *testing.T) {
	mgr := NewSubAgentManager()

	// Create a provider that blocks on Complete until context is cancelled.
	started := make(chan struct{})
	blockProvider := &blockingMockProvider{
		name:    "test",
		model:   "test-model",
		started: started,
	}
	rt := newMockRuntime("/project")

	// Use SpawnAsync which registers in running map.
	mgr.SpawnAsync(context.Background(), SubAgentOpts{
		Name:     "blocker",
		Task:     "block forever",
		Provider: blockProvider,
		Runtime:  rt,
		Logger:   testLogger,
	})

	// Wait until the provider's Complete is called.
	<-started

	// Kill it.
	killed := mgr.Kill("blocker")
	assert.True(t, killed)

	// Killing again should return false.
	killed = mgr.Kill("blocker")
	assert.False(t, killed)
}

// blockingMockProvider blocks on Complete until its context is cancelled.
type blockingMockProvider struct {
	name    string
	model   string
	started chan struct{}
}

func (b *blockingMockProvider) Name() string  { return b.name }
func (b *blockingMockProvider) Model() string { return b.model }
func (b *blockingMockProvider) Complete(ctx context.Context, _ llm.Request, _ llm.StreamCallback) (*llm.Response, error) {
	close(b.started)
	<-ctx.Done()
	return nil, ctx.Err()
}

