func TestSpawnSubAgentTool_ViaAgent(t *testing.T) {
	// Create a sub-provider that the agent will use for spawning.
	subProvider := &mockProvider{
		name:  "sub",
		model: "sub-model",
		responses: []*llm.Response{
			{Content: "Sub-agent result: 5 files found", StopReason: "end",
				Usage: llm.Usage{TotalTokens: 100}},
		},
	}

	// Main provider calls spawn_subagent, then returns final response.
	mainProvider := &mockProvider{
		name:  "main",
		model: "main-model",
		responses: []*llm.Response{
			{
				StopReason: "tool_use",
				ToolCalls: []llm.ToolCall{
					{ID: "call_1", Name: "spawn_subagent", Arguments: `{"task":"Find all Go files","name":"file-finder"}`},
				},
			},
			{Content: "The sub-agent found 5 files.", StopReason: "end"},
		},
	}

	rt := newMockRuntime("/project")
	ag := New(mainProvider, rt, testLogger,
		WithSubAgentProvider(subProvider, testLogger),
	)

	result, err := ag.Run(context.Background(), "Find all Go files using a sub-agent", nil)

	require.NoError(t, err)
	assert.Equal(t, "The sub-agent found 5 files.", result.Response)
	require.Len(t, result.Steps, 1)
	assert.Contains(t, result.Steps[0].Output, "file-finder")
	assert.Contains(t, result.Steps[0].Output, "completed")
	assert.Contains(t, result.Steps[0].Output, "5 files found")
}

